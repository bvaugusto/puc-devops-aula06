class CalculadoraService {

    additionNumber(valor1, valor2) {
        return valor1 + valor2;
    }

    subtractionNumber(valor1, valor2) {
        return valor1 - valor2;
    }

    multiplicationNumber(valor1, valor2) {
        return valor1 * valor2;
    }

    divisionNumber(valor1, valor2) {
        return valor1 / valor2;
    }
}

module.exports = CalculadoraService;